package com.John;

public class Carpet {
    private int dementions;
    private String color;

    public Carpet(int dementions, String color) {
        this.dementions = dementions;
        this.color = color;
    }

    public int getDementions() {
        return dementions;
    }

    public String getColor() {
        return color;
    }
}
